package sistec.bean.produto;

/**
 *
 * @author SERVIDOR
 */
public class GradeProduto {

    private int idGradeProduto;
    private String referencia;
    private String gtin;
    private double valorAvista;
    private double valorAPrazo;
    private double volume;
    private String unidade;
    private int idProduto;

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }

    public int getIdGradeProduto() {
        return idGradeProduto;
    }

    public void setIdGradeProduto(int idGradeProduto) {
        this.idGradeProduto = idGradeProduto;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getGtin() {
        return gtin;
    }

    public void setGtin(String gtin) {
        this.gtin = gtin;
    }

    public double getValorAvista() {
        return valorAvista;
    }

    public void setValorAvista(double valorAvista) {
        this.valorAvista = valorAvista;
    }

    public double getValorAPrazo() {
        return valorAPrazo;
    }

    public void setValorAPrazo(double valorAPrazo) {
        this.valorAPrazo = valorAPrazo;
    }

    public double getVolume() {
        return volume;
    }

    public void setVolume(double volume) {
        this.volume = volume;
    }

    public String getUnidade() {
        return unidade;
    }

    public void setUnidade(String unidade) {
        this.unidade = unidade;
    }

}
