    public void setList(List<Usuario> lst) {
        jTbUsuarios.setModel(new UsuarioTableModel(lst));
        jTbUsuarios.setColumnModel(new UsuariosColumnModel(0));
    }

    private UsuarioTableModel usuarioTableModel;

    public Usuario getUsuarioSelecionado() throws Exception {
        if (jTbUsuarios.getSelectedRow() == -1) {
            throw new Exception("Nao foi selecionado nenhum Usuario");
        }
        return ((UsuarioTableModel) jTbUsuarios.getModel()).getValoresUsuario(jTbUsuarios.getSelectedRow());

    }
